package Model;

public class ContatoModel {
	public String Id;
	public String Nome;
	public String Telefone;
	
	public String getId() {
		return Id;
	}
	public void setId(String id) {
		Id = id;
	}	
	public String getNome() {
		return Nome;
	}
	public void setNome(String nome) {
		Nome = nome;
	}
	public String getTelefone() {
		return Telefone;
	}
	public void setTelefone(String telefone) {
		Telefone = telefone;
	}
}
